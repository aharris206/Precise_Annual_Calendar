function init(){
    
    var rn = new Date();
    var unix = rn.getTime();
    var yearInfo = getYearInfo(unix);
    var year = yearInfo[0];
    var month = 0;
    var m_eq = (yearInfo[1] * 1000);
    var j_sol = (yearInfo[2] * 1000);
    var s_eq = (yearInfo[3] * 1000);
    var d_sol = (yearInfo[4] * 1000);
    var nm_eq = (yearInfo[5] * 1000);
    var prec_anchor = m_eq;
    var upco_anchor = j_sol;
    if (unix > j_sol) {
        if (unix > s_eq) {
            if (unix > d_sol) {
                upco_anchor = nm_eq;
                prec_anchor = d_sol;
                month = 9;
            }
            upco_anchor = d_sol;
            prec_anchor = s_eq;
            month = 6;
        }
        upco_anchor = s_eq;
        prec_anchor = j_sol;
        month = 3;
    }
    let season_len = (upco_anchor - prec_anchor);
    let m_len = (season_len / 3);
    let shift_len = (season_len / 432);
    let span_len = (season_len / 62208);
    let sec_len = (season_len / 8957952);
    startClock(unix, prec_anchor, year, month, m_len, shift_len, span_len, sec_len);
    setTimeout(function(){init()},40);
}

function startClock(unix, prec_anchor, year, m, m_len, shift_len, span_len, sec_len) {
    let month = m;
    let added = 0;
    let shift = 0;
    let span = 0;
    let sec = 0;
    if (unix > (prec_anchor + m_len)) {
        if (unix > (prec_anchor + (m_len * 2))) {
            month += 2;
            added = 2;
        } else {
            month += 1;
            added = 1;
        }
    }
    while (unix > ((prec_anchor + (m_len * added) + (shift_len * shift))) + shift_len) {
        shift += 1;
    }
    while (unix > ((prec_anchor + (m_len * added) + (shift_len * shift) + (span_len * span))) + span_len) {
        span += 1;
    }
    while (unix > ((prec_anchor + (m_len * added) + (shift_len * shift) + (span_len * span) + (sec_len * sec))) + sec_len) {
        sec += 1;
    }
    var month_circle = draw_m(month);
    var shift_hand = draw(shift, 10);
    var span_hand = draw(span, 15);
    var sec_hand = draw(sec, 17.5);
    document.getElementById("month").setAttribute("cx", month_circle[0]);
    document.getElementById("month").setAttribute("cy", month_circle[1]);
    document.getElementById("shift").setAttribute("x2", shift_hand[0]);
    document.getElementById("shift").setAttribute("y2", shift_hand[1]);
    document.getElementById("span").setAttribute("x2", span_hand[0]);
    document.getElementById("span").setAttribute("y2", span_hand[1]);
    document.getElementById("sec").setAttribute("x2", sec_hand[0]);
    document.getElementById("sec").setAttribute("y2", sec_hand[1]);
    document.getElementById("year").innerHTML = "― " + year + " ―";
}

